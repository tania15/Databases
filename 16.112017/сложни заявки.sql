SELECT E.LASTNAME, E.FIRSTNME, D.DEPTNAME
  FROM EMPLOYEE E, DEPARTMENT D
  WHERE E.WORKDEPT = D.DEPTNO
  ORDER BY D.DEPTNAME, E.LASTNAME, E.FIRSTNME;

SELECT E.LASTNAME, E.FIRSTNME, D.DEPTNAME
  FROM EMPLOYEE E JOIN DEPARTMENT D ON E.WORKDEPT = D.DEPTNO
  ORDER BY D.DEPTNAME, E.LASTNAME, E.FIRSTNME;
  
-- 2
SELECT E.LASTNAME, E.FIRSTNME, D.DEPTNAME, E.JOB
  FROM EMPLOYEE E, DEPARTMENT D
  WHERE E.WORKDEPT = D.DEPTNO AND E.WORKDEPT BETWEEN 'A02' AND 'D22' AND JOB <> 'MANAGER'
  ORDER BY D.DEPTNAME, E.LASTNAME, E.FIRSTNME;
  
SELECT E.LASTNAME, E.FIRSTNME, D.DEPTNAME, E.JOB
  FROM EMPLOYEE E JOIN DEPARTMENT D ON E.WORKDEPT = D.DEPTNO
  WHERE E.WORKDEPT BETWEEN 'A02' AND 'D22' AND JOB <> 'MANAGER'
  ORDER BY D.DEPTNAME, E.LASTNAME, E.FIRSTNME;
  
