SELECT D.DEPTNO, M.LASTNAME AS MANAGER, E.LASTNAME AS EMPLOYEE, M.HIREDATE AS M_HIREDATE, 
   E.HIREDATE AS E_HIREDATE 
 FROM EMPLOYEE M, EMPLOYEE E, DEPARTMENT D
 WHERE E.WORKDEPT = 'A00' AND D.MGRNO = M.EMPNO AND E.WORKDEPT = D.DEPTNO AND M.HIREDATE > E.HIREDATE
 ORDER BY EMPLOYEE;
  
SELECT D.DEPTNO, M.LASTNAME AS MANAGER, E.LASTNAME AS EMPLOYEE, M.HIREDATE AS M_HIREDATE, 
   E.HIREDATE AS E_HIREDATE
  FROM EMPLOYEE M JOIN DEPARTMENT D ON D.MGRNO = M.EMPNO JOIN EMPLOYEE E ON E.WORKDEPT = D.DEPTNO
  WHERE E.WORKDEPT = 'A00' AND M.HIREDATE > E.HIREDATE
  ORDER BY EMPLOYEE;
